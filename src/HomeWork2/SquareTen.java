package HomeWork2;

/**
 * this program outputs the table of squares of the first ten whole positive
 * numbers from 0 to 10
 * 
 * @author Ilya
 *
 */

public class SquareTen {

	public static void main(String[] args) {
		for (int j = 0; j < 11; j++) {
			System.out.println(j + " " + j * j);
		}
	}
}
